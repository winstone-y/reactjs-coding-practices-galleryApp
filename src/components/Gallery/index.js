class Gallery extends Component {
  state = {activeImageId: 0}

  displayImage = imageId => {
    this.setState({activeImageId: imageId})
  }

  render() {
    const {activeImageId} = this.state

    return (
      <div className="bg">
        <div>
          <img
            alt={imagesList[activeImageId].imageAltText}
            src={imagesList[activeImageId].imageUrl}
            className="gallery-image"
          />
          <h1 className="gallery-title">Nature Photography</h1>
          <p className="gallery-desc">Nature Photography by Winstone</p>
        </div>
        <ul className="thumb-container">
          {imagesList.map(eachImage => (
            <ThumbnailItem
              displayImage={this.displayImage}
              imageDetails={eachImage}
              isActive={activeImageId === eachImage.id}
              key={eachImage.id}
            />
          ))}
        </ul>
      </div>
    )
  }
}

